// ── Apply language ───────────────────────────────────────
function applyLanguage(lang) {
  const dict = T[lang];
  if (!dict) return;

  // Update html lang attribute
  const langMap = { en: 'en', vi: 'vi', ja: 'ja' };
  document.documentElement.lang = langMap[lang];

  // Page title
  document.title = dict['page.title'] || document.title;

  // Text content
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (dict[key] !== undefined) el.textContent = dict[key];
  });

  // Inner HTML (for elements containing tags like <strong>)
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.dataset.i18nHtml;
    if (dict[key] !== undefined) el.innerHTML = dict[key];
  });

  // Placeholders
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.dataset.i18nPlaceholder;
    if (dict[key] !== undefined) el.placeholder = dict[key];
  });

  // Select elements
  document.querySelectorAll('[data-i18n-select]').forEach(sel => {
    const key = sel.dataset.i18nSelect;
    const opts = dict[key];
    if (!Array.isArray(opts)) return;
    sel.innerHTML = '';
    opts.forEach((text, i) => {
      const opt = document.createElement('option');
      opt.textContent = text;
      if (i === 0) { opt.value = ''; opt.disabled = true; opt.selected = true; }
      sel.appendChild(opt);
    });
  });

  // Update switcher buttons
  document.querySelectorAll('.lang-sw-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  // Persist choice
  localStorage.setItem('lang', lang);
  window.__currentLang = lang;
}

// ── Init on DOM ready ────────────────────────────────────
document.addEventListener('DOMContentLoaded', () => {
  const saved = localStorage.getItem('lang') || 'en';
  applyLanguage(saved);

  document.querySelectorAll('.lang-sw-btn').forEach(btn => {
    btn.addEventListener('click', () => applyLanguage(btn.dataset.lang));
  });
});
